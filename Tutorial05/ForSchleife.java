public class ForSchleife {
	public static void main(String[] args) {
		// Zählervariable; Bedingung; Hochzählung;
		// Variante1 für das Hochzählen z.B. i = i + 1
		// Variante2 für das Hochzählen z.B. i += 1
		// Variante3 für das Hochzählen z.B. i++
		for (int i = 0; i <= 25; i += 5) {
			System.out.println(i);
		}
	}
}
